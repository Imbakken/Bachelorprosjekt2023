      <h1>Avfall og kildesortering</h1>
      <p>
        Riktig håndtering av avfall er en del av vårt reglement for
        studentboliger. Eventuelle kostnader som følge av brudd på Reglement for
        studentboliger belastes leietaker, jf. leiekontraktens § 16-1. Brudd på
        Reglement for studentboliger kan medføre skriftlig advarsel, oppsigelse,
