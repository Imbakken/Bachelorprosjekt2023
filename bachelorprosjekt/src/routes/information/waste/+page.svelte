        §§16-3 og 16-4.
      </p>
      <ul>
        <li>
          Avfall skal aldri oppbevares i trapperom, oppganger, felleskorridorer
          eller kjellerganger.
        </li>
        <li>Avfall skal pakkes forsvarlig og kastes på anviste steder.</li>
        <li>Avfall må aldri setter utenfor container.</li>
        <li>
          Det er kildesortering på alle studentbyer og –boliger, og alle
          leietakere skal sortere søppel før kasting.
        </li>
      </ul>
      <h2>Sortering av avfall</h2>
      <h3>Husholdningsavfall/restavfall/matavfall</h3>
      <ul>
        <li>Denne typen avfall kastes i beholder merket Restavfall.</li>
        <li>
          Avfallet må legges i plastposer. Vanlige plastposer fra
          dagligvarebutikk går bra, veldig store handleposer og svartsekker må
          ikke benyttes, fordi de ikke passer i innkastet til avfallsbeholder.
        </li>
        <li>
          Du må aldri sette avfallet ved siden av avfallsbeholder, i
          trappegangen eller annet sted utenfor egen bolig, fordi denne typen
          avfall tiltrekker seg insekter, fugler, rotter og andre skadedyr.
        </li>
      </ul>
      <h3>Plastemballasje</h3>
      <ul>
        <li>Denne typen avfall kastes i beholder merket Plastemballasje.</li>
        <li>
          Plastemballasje som har inneholdt mat må alltid skylles grundig før
          det kastes.
        </li>
        <li>
          Avfallet må legges i plastposer. Vanlige plastposer fra
          dagligvarebutikk går bra, veldig store handleposer og svartsekker må
          ikke benyttes, fordi de ikke passer i innkastet til avfallsbeholder.
        </li>
        <li>
          Du må aldri sette avfallet ved siden av avfallsbeholder, i
          trappegangen eller annet sted utenfor egen bolig, fordi denne typen
          avfall tiltrekker seg insekter, fugler, rotter og andre skadedyr.
        </li>
      </ul>
      <h3>Papp/papir</h3>
      <ul>
        <li>Denne typen avfall kastes i beholder merket Papp Papir.</li>
        <li>Skal ikke kastes i plastposer.</li>
        <li>
          Klipp eller riv i stykker pappen hvis det er vanskelig å få den ned i
          beholderen.
        </li>
        <li>
          Ta kontakt med vaktmester dersom du har pappavfall som ikke får plass
          i beholderen, så hjelper vi deg å få det fjernet. Du finner
          kontaktinformasjon i menyen til høyre eller nederst på siden.
        </li>
      </ul>
      <h3>Spesialavfall/avfall som inneholder elektroniske komponenter</h3>
      <ul>
        <li>Må ikke settes i trappegangen eller ved våre avfallstasjoner.</li>
        <li>
          Kastes i egen container merket farlig avfall som finnes på
          parkeringsplassen.
        </li>
      </ul>
      <h3>Engangsgriller</h3>
      <ul>
        <li>
          Det er strengt forbudt å kaste engangsgriller i restavfallet, på grunn
          av stor brannfare.
        </li>
        <li>
          På alle våre studentbyer som har grillplasser er det utplassert egne
          avfallsbokser for kasting av brukte engangsgriller.
        </li>
      </ul>
      <h3>Møbler og annet avfall som ikke har dedikert avfallsbeholder</h3>
      <ul>
        <li>
          Ta kontakt med vaktmester for å avtale henting. Du finner
          kontaktinformasjon i menyen til høyre eller nederst på siden. Vi
          henter kun i normal åpningstid.
        </li>
      </ul>
